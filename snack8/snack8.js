// e calcola la somma di tutte le cifre che compongono il numero. (modificato) 

var numero = Number(prompt('numero di quattro cifre..'));

for (var i = 0; i < numero.length; i++) {
    var somma = numero[i];
}
console.log('il tuo numero è ' + numero + somma);